function jourSemaine (a){
    if (a==0){
        return "lundi";
    }
    else if (a==1){
        return "mardi";
    }
    else if (a==2){
        return "mercredi";
    }
    else if (a==3){
        return "jeudi";
    }
    else if (a==4){
        return "vendredi";
    }
    else if (a==5){
        return "samedi";
    }
    else if (a==6){
        return "dimanche";
    }
    else {
        return "veullez entrez un nombre entre 0 et 6 !!!";
    }
}
